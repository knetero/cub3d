#include "../includes/cub3d.h"

void	freemap(t_struct **s)
{
	int	i;

	i = 0;
	if ((*s)->map)
	{
		while ((*s)->map[i])
		{
			free((*s)->map[i++]);
		}
		free((*s)->map);
	}
}
